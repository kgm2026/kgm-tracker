-- ─────────────────────────────────────────────
-- 4. contractor_schedules — new table
-- ─────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS contractor_schedules (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  contractor_id TEXT NOT NULL,        -- matches contractors.contractor_id
  project_id TEXT NOT NULL,
  milestone TEXT NOT NULL,            -- e.g. "Foundation Complete"
  amount NUMERIC(14,2) NOT NULL DEFAULT 0,
  due_date DATE,
  paid_date DATE,
  status TEXT NOT NULL DEFAULT 'Pending', -- Pending | Paid | Overdue
  notes TEXT,
  doc_url TEXT,                       -- Supabase Storage URL for attached PDF/image
  doc_name TEXT,                      -- original filename
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc', now())
);

ALTER TABLE contractor_schedules ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Enable all on contractor_schedules" ON contractor_schedules
  FOR ALL USING (true) WITH CHECK (true);

CREATE INDEX IF NOT EXISTS idx_cs_contractor ON contractor_schedules(contractor_id);
CREATE INDEX IF NOT EXISTS idx_cs_project ON contractor_schedules(project_id);
