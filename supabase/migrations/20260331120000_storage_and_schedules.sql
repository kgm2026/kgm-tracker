-- 1. Storage RLS policies (buckets already created via API)
-- ─────────────────────────────────────────────

-- Allow public read on all three buckets
CREATE POLICY "Public read invoices" ON storage.objects
  FOR SELECT USING (bucket_id = 'invoices');

CREATE POLICY "Public read contractor-docs" ON storage.objects
  FOR SELECT USING (bucket_id = 'contractor-docs');

CREATE POLICY "Public read site-photos" ON storage.objects
  FOR SELECT USING (bucket_id = 'site-photos');

-- Allow anon insert/update/delete (app uses anon key; RLS is our only gate)
CREATE POLICY "Anon insert invoices" ON storage.objects
  FOR INSERT WITH CHECK (bucket_id = 'invoices');

CREATE POLICY "Anon update invoices" ON storage.objects
  FOR UPDATE USING (bucket_id = 'invoices');

CREATE POLICY "Anon delete invoices" ON storage.objects
  FOR DELETE USING (bucket_id = 'invoices');

CREATE POLICY "Anon insert contractor-docs" ON storage.objects
  FOR INSERT WITH CHECK (bucket_id = 'contractor-docs');

CREATE POLICY "Anon update contractor-docs" ON storage.objects
  FOR UPDATE USING (bucket_id = 'contractor-docs');

CREATE POLICY "Anon delete contractor-docs" ON storage.objects
  FOR DELETE USING (bucket_id = 'contractor-docs');

CREATE POLICY "Anon insert site-photos" ON storage.objects
  FOR INSERT WITH CHECK (bucket_id = 'site-photos');

CREATE POLICY "Anon update site-photos" ON storage.objects
  FOR UPDATE USING (bucket_id = 'site-photos');

CREATE POLICY "Anon delete site-photos" ON storage.objects
  FOR DELETE USING (bucket_id = 'site-photos');

